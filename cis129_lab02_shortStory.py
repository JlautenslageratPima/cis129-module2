# Prints a neverending short story about rats
import time
crazy = True
while crazy == True:
  print('Crazy? I Was Crazy Once.')
  time.sleep(1)
  print('They Locked Me In A Room.')
  time.sleep(1)
  print('A Rubber Room.')
  time.sleep(1)
  print('A Rubber Room With Rats.')
  time.sleep(1)
  print('And Rats Make Me Crazy.')
  time.sleep(1)

